package p07_Loops;
public class p01_For {

	public static void main(String[] args) {

		// for loop syntax:
		// for (initialization; condition; update) {
		// // code to be executed
		// }

		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
	}

}
